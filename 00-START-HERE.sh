Что такое ansible-pull?
  • Инструмент для управления конфигурацией машин
  • Pull-модель (машина сама забирает конфиг из GitHub)
  • Работает без SSH доступа администратора
  • Масштабируется на тысячи машин
  • Автоматизируется через cron

Что здесь реализовано?
  ✓ Базовая конфигурация (пакеты, директории)
  ✓ Веб-сервер (Nginx)
  ✓ Мониторинг и логирование
  ✓ Полная документация
  ✓ Красивые демонстрационные скрипты

